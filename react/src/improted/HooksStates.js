// . In React, state refers to an object that holds data or information about
// the component. State is managed within the component (just like
// variables declared in a function). However, unlike regular variables, when
// state changes, React re-renders the component to reflect these changes,
// keeping the user interface in sync with the data.

// . State is dynamic and mutable, meaning it can change over tir
// in response to user actions, server responses, or other event