



// React Hooks Rule

// . Hooks let you use different React features from your components.

// . It starts with use

// . Hooks can only be used at the top level of your component.

// . Do not call Hooks inside loops, conditions, or nested functions.

// . Call Hooks only from React function components or custom Hook. Do not call Hooks from regular
// JavaScript functions or in Class components.

// . You can also create your own custom hooks by creating functions starting with use
// ensures that React can identify it as a Hook. (ex. useFetch, useForm)

// . Ensure Hooks are Called in the Same Order. This allows React to properly preserve t
// between re-renders.

// . (useState, useEffects, useReducer)